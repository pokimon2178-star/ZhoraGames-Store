package com.example.project2178

// Класс-модель для представления одного товара (игры)
class Item(
    val id: Int,             // Уникальный идентификатор товара (числовой ID из базы данных)
    val image: String,       // Название ресурса картинки или путь к ней
    val title: String,       // Название товара (например, заголовок игры)
    val description: String, // Краткое описание для отображения в списке
    val full_text: String,   // Полный текст описания для экрана подробностей
    val price: Int,          // Стоимость товара (целое число)
    val genre: String        // Жанр игры (например, "Экшен", "РПГ")
)